#include "../include/libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t	i;
	char	*cast_ptr;

	i = 0;
	cast_ptr = (char *)s;
	while (i < n)
		cast_ptr[i++] = '\0';
}
